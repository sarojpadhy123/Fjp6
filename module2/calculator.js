function add(num1, num2) {
    return num1 + num2;
}

function subtract(num1, num2) {
    return num1 - num2;
}

function divide(num1, num2) {
    return num1 / num2;
}

function multiply(num1, num2) {
    return num1 * num2
}
module.exports = {
    addition: add,
    subtraction: subtract,
    divison: divide,
    multiplication: multiply
}